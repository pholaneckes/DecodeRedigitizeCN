                        {
                            gfx.DrawImage(textBoxUpFull, 0, 0, 700, 440);
                            x = 106;
                            y = 124;
                            ups = true;
                            z = 2.2f;
                            scale = 1.43f;
                        }
                        else
                        {
                            gfx.DrawImage(background, 0, 0);

                            gfx.DrawImage(textBox, 61, 350 - textBox.Height, 566, 146);
                            x = 85;
                            y = 420 - textBox.Height - 17;
                            z = 6;
                            scale = 1.65f;
                            ups = false;
                        }

                        // Text
                        Rectangle rectText = new Rectangle(32, 20, 336, 44);

                        bool cmd = false;
                        string param = "", temp = "";
                        int skip = 0, j = 0, padding = 0;

                        font.SetColor(Color.FromArgb(255, 255, 255, 255));
                        gfx.InterpolationMode = InterpolationMode.Bicubic;
                        
                        string p2 = whoSay;
                        if (nameDic.ContainsKey(whoSay))
                        {
                            string charName = nameDic[whoSay];
                            p2 = charName;
                        }

                        float x2 = 470;

                        int tm = 0;
                        int yp = 0;
                        bool min_lin = false;
                        bool name_replace = false;
                        bool dic = false;
                        bool wit = false;
                        string replacing = "";
                        foreach (char c in p)
                        {
                            if (min_lin)
                            {
                                if(c == 'n')
                                {
                                    yp += ups ? 42 : 46;
                                    x = ups ? 106 : 85;
                                    tm = 0;

                                }
                                min_lin = false;
                            }else if (name_replace)
                            {
                                if(c == 'n')
                                {
                                    dic = true;
                                }
                                else if (c == 'w')
                                {
                                    wit = true;
                                }
                                //else if (c == 'p')
                                //{
                                //   yp += ups ? 42 : 46;
                                //    yp += ups ? 42 : 46;
                                //    x = ups ? 106 : 85;
                                //    tm = 0;
                                //    pp = true;
                                //}
