                                {
                                    name_replace = true;
                                }
                                else
                                {
                                    font.Draw(c, gfx, tm * z + x, y + yp, scale, scale);
                                    x += font.GetWidthInfo(c).char_width * scale;
                                    tm++;
                                }
                            }
                        }
                        int t2m = 0;
                        foreach (char c in p2)
                        {
                            font.Draw(c, gfx, t2m*3+ x2, y - 52, 1.5f, 1.5f);
                            x2 += font.GetWidthInfo(c).char_width * scale;
                            t2m++;
                        }
                    }

                    pages.Add(img);
                }
            }

            return pages;
        }

