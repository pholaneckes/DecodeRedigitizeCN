        private string GetBetweenStr(string str, string str1, string str2)
        {
            int i1 = str.IndexOf(str1);
            if (i1 < 0) 
            {
                return "";
            }

            int i2 = str.IndexOf(str2, i1 + str1.Length);
            if (i2 < 0) 
            {
                return "";
            }

            return str.Substring(i1 + str1.Length, i2 - i1 - str1.Length);
        }

