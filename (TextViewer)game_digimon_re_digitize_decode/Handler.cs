            bool pss = false;
            int oi = 0;
                for (int i = 0; i < list.Count; i++)
            {
                curstr += list[i];
                if (list[i].Contains("<p>"))
                {
                    pagestr.Add(curstr.Replace("<p>", ""));
                    oi++;
                    curstr = "";
                }
                else if(list[i].Contains("<pn>"))
                {
                    pagestr.Add(curstr.Replace("<pn>", ""));
                    pss = true;
                    if (i % hanh != hangh)
                    {
                        oi++;
                    }
                    curstr = "";
                }
                else if((i-oi) % hanh == hangh)
                {
                    pagestr.Add(curstr);
                    curstr = "";
                }

                else
                {
                    if (pss) {
                        pss = false;
                        curstr += "\\n";
                    }
                    else
                    {
                        curstr += "\\n";
                    }
                }
            }

