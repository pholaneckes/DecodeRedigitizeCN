                                    if (!wit)
                                    {
                                        string replaced;
                                        if (nameDic.ContainsKey(replacing))
                                        {
                                            replaced = nameDic[replacing];
                                        }
                                        else
                                        {
                                            replaced = "<n" + replacing + ">";
                                        }
